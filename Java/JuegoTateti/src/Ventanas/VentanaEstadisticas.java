    private void cargarDatosTabla(){
        tabla.addColumn("Victorias de "+usuario1);
        tabla.addColumn("Victorias de "+usuario2);
        tabla.addColumn("Partidas empatadas");
        
        String [] fila = {String.valueOf(contBoca), String.valueOf(contRiver), String.valueOf(contEmpates)};
        
        tabla.addRow(fila);
    }
    
